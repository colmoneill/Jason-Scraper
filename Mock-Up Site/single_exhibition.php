				<h2>friends & family</h2>
				<p class="presentation__text">
			Just as friendships float in and out of focus, so too do the objects that spur memories of them. Photographs, scribbled notes, and scraps gradually become totems to the people we care about - as if all one’s friends have been assigned corresponding imagery to be framed, repeated: an ode in the form of a mantra. In graffiti culture, a writer’s name rarely appears alone; a piece is always flanked by dedications to the writer’s community: His or her crew, lovers, landlord, parents, pets. Names become nicknames, nicknames become inside-jokes, inside jokes become fodder for new imagery, and thus a visual language to describe and pay homage to friendships crystallizes.</p>
<p class="presentation__text">This dialogue between relationships and mark-making, objects and memory is central to Jack Greer’s approach, and forms the basis for the works he will present in his first solo exhibition at Galerie Rodolphe Janssen.</p>
<p class="presentation__text">The exhibition unfolds with a photo-realistic painting of a quintessential American landscape: a mountainous Colorado valley foregrounded by ice-age boulders. But here, these ancient rocks are interrupted by spray-painted tags, indel- ible reminders of a passing youthful presence. Painted from a photograph taken while Jack was traveling to see his parents in California, the meticulous process required to portray the scene became a quest to understand why Jack has held on to the photo for so long. As it turns out, the painting is a dedication to his mother, a Colorado native and an artist in her own right, who is the lush back- drop to his defaced rock.</p>
<p class="presentation__text">Other works similarly unpack the importance of relationships to Greer. Patch-work canvases, sewn together from friends’ discarded works at the Still House Group studio in Brooklyn, are as much anthropological studies of collective processes as they are portraits of friends. The form these works take is determined by past processes of his artist subjects: scraps and discarded versions of Louis Eisner’s Mountain Stream Ringtone works (recently exhibited at Rodolphe Janssen) impose gradients of blue throughout some works in the series, while others take on the uncanny hue of oxidised pennies from Nick Darmstaedter’s Penny Pieces.
				</p>
