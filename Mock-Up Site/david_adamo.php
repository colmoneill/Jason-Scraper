				<p class="presentation__text">
			David Adamo’s oeuvre incorporates sculpture, performance, installation, and video. In his sculptures, Adamo has often manipulated familiar manufactured objects: canes, baseball bats, tools and axes carved and hacked away in such a way that they become disconnected from their intended use, the inherent strength of each transformed into a fragile ghost of what we expect.</p>
<p class="presentation__text">Adamo’s new work pushes these transformations in new directions, not only subverting an object’s intrinsic use but also playing with shifts in size and mate- rial. Squashed tomatoes are cast in bronze; rolled-up carpets are carved from trunks of cedar wood. The sculptures can take on impressive dimensions or become tiny, inconspicuous objects.</p>
<p class="presentation__text">The objects Adamo continuously transforms into artworks are always familiar objects, like household radiators, erasers and even cookies or pieces of fruit. They are domestic objects, objects that remind the artist of home, of family, of his child.</p>
<p class="presentation__text">Through his transformation or reproduction of these objects, Adamo deprives them of their primary function. Radiators are made of materials that are unable to heat, cookie crumbs are actually meticulously painted plaster, and tomatoes are cast in bronze, so they are no longer edible. Also his older works are based upon the same principle: everyday utensils such as axes or hammers become shattered pieces of wood, baseball bats are used as wooden floor covering. These objects become fragile instead of frightening.</p>
<p class="presentation__text">Adamo also deliberately focuses on the scale of the objects he reproduces: the radiators are enlarged or immensly reduced in size; prestigious entrance gates become narrow wooden doors leading to nowhere. Massive termite mounds are reduced to more manageable sizes, which takes away a lot of their scaryness. The viewer seems to enter a universe parallel to the one Lewis Caroll created..
